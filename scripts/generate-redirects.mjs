// Postbuild: dual-serve every page at /<path> AND /<path>/ with 200,
// then write minimal _redirects.
//
// Why: with build.format:'file', dist/<path>.html serves /<path> at 200, and
// CF auto-308-strips trailing slashes (/<path>/ → /<path>). But many share-link
// crawlers (WeChat, QQ, older scrapers) DON'T follow redirects — they see the
// 308 with empty body and fall back to bare-domain previews (no OG image, no
// page title). To fix, we also write dist/<path>/index.html so CF serves
// /<path>/ at 200 directly. Canonical still points to /<path> (no slash) so
// search engines deduplicate properly.
//
// Locale-root flatten: src/pages/<locale>/index.astro outputs dist/<locale>.html
// already (Astro flattens it under build.format:'file'). Nothing to do there.

import { copyFileSync, existsSync, mkdirSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const distDir = fileURLToPath(new URL('../dist', import.meta.url));

// Walk dist/ and find every <path>.html that's a real page route. Skip:
//   - dist/index.html (root, served at /)
//   - dist/404.html (catch-all 404, never a direct route)
//   - dist/<locale>/404.html (per-locale 404)
function walkHtml(dir, prefix = '') {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const abs = join(dir, entry);
    const rel = prefix ? `${prefix}/${entry}` : entry;
    if (statSync(abs).isDirectory()) {
      out.push(...walkHtml(abs, rel));
    } else if (entry.endsWith('.html')) {
      out.push(rel);
    }
  }
  return out;
}

const SKIP = /^(index\.html|404\.html|.+\/404\.html)$/;
const htmlFiles = walkHtml(distDir).filter((p) => !SKIP.test(p));

let duplicated = 0;
for (const rel of htmlFiles) {
  const src = join(distDir, rel);
  // dist/maps.html → dist/maps/index.html
  // dist/en/maps.html → dist/en/maps/index.html
  // dist/en.html → dist/en/index.html (already exists from Astro, skip)
  const targetDir = src.replace(/\.html$/, '');
  const targetFile = join(targetDir, 'index.html');
  if (existsSync(targetFile)) continue;
  mkdirSync(targetDir, { recursive: true });
  copyFileSync(src, targetFile);
  duplicated++;
}

// _redirects: only the 404 catch-alls remain. CF natively handles trailing-slash
// behavior now that /<path>/ returns 200 directly.
const redirects = [
  '# Generated by scripts/generate-redirects.mjs — do not edit by hand.',
  '',
  '# Per-locale 404 fallback',
  '/en/* /en/404 404',
  '/ko/* /ko/404 404',
  '/* /404 404',
  '',
];
writeFileSync(join(distDir, '_redirects'), redirects.join('\n'));

console.log(`✓ Duplicated ${duplicated} routes for slash-tolerant serving; wrote _redirects`);
