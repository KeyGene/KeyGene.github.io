// Generates dist/_redirects from the built dist/ directory.
// Runs as `postbuild` after `astro build` (locally and on CF Pages).
//
// For every dist/<path>/index.html (except the root), emits:
//   /<path>/ /<path> 301         — strip trailing slash
//   /<path> /<path>/index.html 200  — bypass CF Pages auto-308 to dir form
//
// Locale roots /en /ko are included. Top-level /404.html is skipped (Astro
// emits it as a flat file, no 308 issue). Locale 404s (/en/404, /ko/404) are
// skipped because they're served as catch-all 404 bodies, not direct routes.

import { readdirSync, statSync, writeFileSync } from 'node:fs';
import { join, posix } from 'node:path';
import { fileURLToPath } from 'node:url';

const distDir = fileURLToPath(new URL('../dist', import.meta.url));

function findIndexHtml(dir, prefix = '') {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const abs = join(dir, entry);
    if (statSync(abs).isDirectory()) {
      out.push(...findIndexHtml(abs, posix.join(prefix, entry)));
    } else if (entry === 'index.html' && prefix) {
      out.push(prefix);
    }
  }
  return out;
}

const routes = findIndexHtml(distDir)
  .filter((r) => !/(^|\/)404$/.test(r))
  .sort();

const lines = [
  '# Generated by scripts/generate-redirects.mjs — do not edit by hand.',
  '',
  '# 1. Strip trailing slashes (301 to no-slash canonical)',
  ...routes.map((r) => `/${r}/ /${r} 301`),
  '',
  '# 2. Serve dir index.html at no-slash paths (200 rewrite — bypass CF auto-308)',
  ...routes.map((r) => `/${r} /${r}/index.html 200`),
  '',
  '# 3. Per-locale 404 fallback',
  '/en/* /en/404 404',
  '/ko/* /ko/404 404',
  '/* /404 404',
  '',
];

writeFileSync(join(distDir, '_redirects'), lines.join('\n'));
console.log(`✓ Wrote dist/_redirects with ${routes.length} routes`);
