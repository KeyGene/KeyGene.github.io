// Postbuild: flatten locale-root index.html and write minimal _redirects.
//
// With astro build.format: 'file', most pages output as dist/<path>.html (flat),
// which CF Pages serves at /<path> directly with no auto-308. CF natively
// 308-strips trailing slashes when only the .html file exists.
//
// Locale roots are special: src/pages/<locale>/index.astro outputs to
// dist/<locale>/index.html (Astro can't flatten an index inside a subdir).
// We copy it to dist/<locale>.html so CF treats /<locale> as a flat-file route
// and stops auto-308-ing it to /<locale>/.

import { copyFileSync, existsSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

const distDir = fileURLToPath(new URL('../dist', import.meta.url));

const LOCALES = ['en', 'ko'];
let flattened = 0;
for (const locale of LOCALES) {
  const src = join(distDir, locale, 'index.html');
  const dst = join(distDir, `${locale}.html`);
  if (existsSync(src)) {
    copyFileSync(src, dst);
    flattened++;
  }
}

// _redirects: only the 404 catch-alls remain. CF handles trailing-slash
// normalization natively for flat .html routes.
const redirects = [
  '# Generated by scripts/generate-redirects.mjs — do not edit by hand.',
  '',
  '# Per-locale 404 fallback',
  '/en/* /en/404 404',
  '/ko/* /ko/404 404',
  '/* /404 404',
  '',
];
writeFileSync(join(distDir, '_redirects'), redirects.join('\n'));

console.log(`✓ Flattened ${flattened} locale roots; wrote minimal _redirects`);
